public static class Rekenmachine
{
  public static decimal Optellen(int input1, int input2)
  {
    return input1 + input2;
  }

  public static decimal Aftrekken(int input1, int input2)
  {
    return input1 - input2;
  }
  public static decimal Vermenigvuldigen(int input1, int input2)
  {
    return input1 * input2;
  }
  public static decimal Delen(int input1, int input2)
  {
    return input1 / input2;
  }
}